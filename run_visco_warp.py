"""
run_visco_warp.py -- warpage / spring-back variant of the viscoelastic
curing model.

Differences vs run_visco.py / convert_visco_inp.py:
  - ASYMMETRIC layup [0_8/90_8] (bottom half fiber along x, top half fiber
    along y), assigned by element-centroid z -- an unsymmetric cross-ply is
    the classic warpage driver (the original [0_4/90_8/0_4] is symmetric
    and would spring back to ~flat).
  - a second *STEP releases the cure constraints (top-face platen uz=0 and
    the prescribed surface temperature): only the two symmetry planes plus
    uz=0 at the plate-centre bottom node remain (isostatic), so the locked
    -in residual stresses re-equilibrate into free deformation = warpage.
  - no .frd field output (keeps files small); warpage is read from
    *NODE PRINT displacements of the bottom face in the .dat.

Usage:
  python run_visco_warp.py               # deck + (compile) + solve + plot
  python run_visco_warp.py --compile     # force recompiling libviscmod.dll
  python run_visco_warp.py --plot-only   # post-process an existing .dat
"""
