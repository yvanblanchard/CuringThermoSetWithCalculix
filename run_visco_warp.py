def build_dll():
    cmd = [str(GFORTRAN), "-shared", "-O2", "-ffixed-form",
           "-ffixed-line-length-none", "-Wl,--export-all-symbols",
           "-J", "_visco_build", "-I", "_visco_build",
           SRC_FOR, "umat_shim.f", "-o", "libviscmod.dll"]
    (HERE / "_visco_build").mkdir(exist_ok=True)
    print("[build]", " ".join(cmd))
    subprocess.run(cmd, cwd=HERE, check=True)


def run_ccx():
    print(f"[ccx] {CCX} -i {JOB}")
    r = subprocess.run([str(CCX), "-i", JOB], cwd=HERE)
    print("[ccx] exit", r.returncode)
    if r.returncode:
        sys.exit(r.returncode)


def parse_displacements():
    """-> {time: {node: (ux, uy, uz)}} from the NODE PRINT blocks."""
    hdr = re.compile(r"displacements .* time\s+([\d.E+\-]+)", re.I)
    out, cur = {}, None
    with open(HERE / f"{JOB}.dat", encoding="latin-1") as f:
        for raw in f:
            line = raw.strip()
            m = hdr.search(line)
            if m:
                cur = out.setdefault(float(m.group(1)), {})
                continue
            if cur is not None and line and line[0].isdigit():
                p = line.split()
                try:
                    cur[int(p[0])] = (float(p[1]), float(p[2]), float(p[3]))
                except (ValueError, IndexError):
                    cur = None
            elif cur is not None and line and re.match(r"[a-zA-Z]", line):
                cur = None
    return out


def make_plot(coords):
    disp = parse_displacements()
    times = sorted(disp)
    t_cure = max(t for t in times if t <= 18096.0)   # end of step 1
    t_rel = times[-1]                                 # end of release step
    print(f"[dat] displacement frames: {len(times)}, "
          f"cure end t={t_cure:.0f} s, release end t={t_rel:.0f} s")

    def field(t):
        # bottom-face (z=0) nodes only: step-2 frames contain all nodes
        d = disp[t]
        nodes = sorted(n for n in d if coords[n][2] < 1e-6)
        x = np.array([coords[n][0] for n in nodes])
        y = np.array([coords[n][1] for n in nodes])
        uz = np.array([d[n][2] for n in nodes])
        return x, y, uz

    x, y, uz_c = field(t_cure)
    _, _, uz_r = field(t_rel)

    # full-mesh text export: coordinates + displacements [mm], final frame
    d = disp[t_rel]
    txt = HERE / f"{JOB}_nodes_disp.txt"
    with open(txt, "w", encoding="utf-8") as f:
        f.write("# node    x[mm]        y[mm]        z[mm]        "
                "ux[mm]       uy[mm]       uz[mm]   (after release, "
                f"t={t_rel:.0f} s)\n")
        for n in sorted(d):
            cx, cy, cz = (v * 1e3 for v in coords[n])
            ux, uy, uz = (v * 1e3 for v in d[n])
            f.write(f"{n:6d} {cx:12.5f} {cy:12.5f} {cz:12.5f} "
                    f"{ux:12.6f} {uy:12.6f} {uz:12.6f}\n")
    print(f"[txt] {txt.name}: {len(d)} nodes exported")
    # warpage = bottom-face out-of-plane deviation, relative to plate
    # centre; mesh/displacements are in meters -> convert to mm
    ic = np.argmin(x ** 2 + y ** 2)
    wc = (uz_c - uz_c[ic]) * 1e3
    wr = (uz_r - uz_r[ic]) * 1e3
    xs, ys = x * 1e3, y * 1e3

    print(f"[warp] constrained (end of cure): min {wc.min():.4f} / "
          f"max {wc.max():.4f} mm")
    print(f"[warp] released   (spring-back) : min {wr.min():.4f} / "
          f"max {wr.max():.4f} mm  (peak-to-peak "
          f"{wr.max() - wr.min():.4f} mm over the quarter plate)")

    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    fig = plt.figure(figsize=(11, 8))
    ax1 = fig.add_subplot(2, 2, 1)
    ax2 = fig.add_subplot(2, 2, 2)
    vmax = max(abs(wr).max(), 1e-9)
    for ax, w, title in ((ax1, wc, "end of cure (platen on)"),
                         (ax2, wr, "after release (demold)")):
        tc = ax.tricontourf(xs, ys, w, levels=21, cmap="RdBu_r",
                            vmin=-vmax, vmax=vmax)
        ax.set_aspect("equal")
        ax.set_title(f"bottom-face uz [mm], {title}", fontsize=10)
        ax.set_xlabel("x [mm] (0 = plate centre)")
        ax.set_ylabel("y [mm]")
        fig.colorbar(tc, ax=ax, shrink=0.85)

    # profiles along x-edge (y=0), y-edge (x=0) and diagonal
    ax3 = fig.add_subplot(2, 1, 2)
    for sel, lab, st in ((ys < 1e-3, "along x (y=0)", "-"),
                         (xs < 1e-3, "along y (x=0)", "--")):
        r = np.where(sel, np.hypot(xs, ys), np.nan)
        o = np.argsort(r[sel])
        ax3.plot(np.hypot(xs, ys)[sel][o], wr[sel][o], "b" + st, lw=1.8,
                 label=f"released, {lab}")
        ax3.plot(np.hypot(xs, ys)[sel][o], wc[sel][o], "0.6" + st, lw=1.2,
                 label=f"constrained, {lab}")
    ax3.set_xlabel("distance from plate centre [mm]")
    ax3.set_ylabel("uz - uz(centre) [mm]")
    ax3.grid(True, alpha=0.3)
    ax3.legend(fontsize=9)
    ax3.set_title("warpage profiles, bottom face", fontsize=10)

    fig.suptitle("Asymmetric [0_8/90_8] laminate -- residual-stress "
                 "spring-back after demold (viscoelastic model)",
                 fontsize=12, fontweight="bold")
    fig.tight_layout()
    out = HERE / "curing_visco_warpage.png"
    fig.savefig(out, dpi=110)
    print("[plot] saved", out)


def main():
    coords = None
    if "--plot-only" not in sys.argv:
        coords = gen_deck()
        dll = HERE / "libviscmod.dll"
        if "--compile" in sys.argv or not dll.exists():
            build_dll()
        else:
            print(f"[build] reusing existing {dll.name} "
                  "(pass --compile to force a rebuild)")
        run_ccx()
    if coords is None:
        coords = read_source()[2]
    make_plot(coords)


if __name__ == "__main__":
    main()
